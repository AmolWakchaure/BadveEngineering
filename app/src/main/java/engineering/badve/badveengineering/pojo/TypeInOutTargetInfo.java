public class TypeInOutTargetInfo {

    private String designationType;
    private String inTarget;
    private String inCount;
    private String outCount;
    private String shiftName;
    private String outEntryStatus;
    private String dateDate;

    public String getDesignationType() {
        return designationType;
    }

    public void setDesignationType(String designationType) {
        this.designationType = designationType;
    }

    public String getInTarget() {
        return inTarget;
    }

    public void setInTarget(String inTarget) {
        this.inTarget = inTarget;
    }

    public String getInCount() {
        return inCount;
    }

    public void setInCount(String inCount) {
        this.inCount = inCount;
    }

    public String getOutCount() {
        return outCount;
    }

    public void setOutCount(String outCount) {
        this.outCount = outCount;
    }

    public String getShiftName() {
        return shiftName;
    }

    public void setShiftName(String shiftName) {
        this.shiftName = shiftName;
    }


    public String getOutEntryStatus() {
        return outEntryStatus;
    }

    public void setOutEntryStatus(String outEntryStatus) {
        this.outEntryStatus = outEntryStatus;
    }

    public String getDateDate() {
        return dateDate;
    }

    public void setDateDate(String dateDate) {
        this.dateDate = dateDate;
    }
}
